from turingmachine import TuringMachine

Q = ('A', 'B', 'C', 'D', 'E', 'F', 'G', 'H')
G = (0, 1, '#', ' ', '.')
S = (0, 1, '#', '.')
B = (' ')
Start = ('A')
Finish = ('H')
Delta = {
        'A' : {0: ['A', 0, 'R'], 1: ['A', 1, 'R'], B: ['B', '#', 'L']},
        'B' : {0: ['B', 0, 'L'], 1: ['B', 1, 'L'], '#': ['B', '#', 'L'], B: ['C', B, 'R']},
        'C' : {0: ['D', '.', 'R'], 1: ['E', '.', 'R'], '#': ['H', '#', 'N']},
        
        'D' : {0: ['D', 0, 'R'], 1: ['D', 1, 'R'], '#': ['D', '#', 'R'], B: ['F', 0, 'L']},
        'E' : {0: ['E', 0, 'R'], 1: ['E', 1, 'R'], '#': ['E', '#', 'R'], B: ['G', 1, 'L']},
        
        'F' : {0: ['F', 0, 'L'], 1: ['F', 1, 'L'], '#': ['F', '#', 'L'], '.': ['C', 0, 'R']},
        'G' : {0: ['G', 0, 'L'], 1: ['G', 1, 'L'], '#': ['G', '#', 'L'], '.': ['C', 1, 'R']},
        }



M = TuringMachine(Q, S, G, B, Start, Finish, Delta, [1, 1, 0, 1, 0, 1])
print(M)
M.run(True, 4995, 5015)

