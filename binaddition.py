from turingmachine import TuringMachine

Q = (0, 1, 2, 3, 4, 5, "H")
G = (0, 1," ")
S = (0, 1)
B = (" ")
Start = (0)
Finish = ("H")
Delta = {
        0 : {0 : [0, 0, 'R'], 1 : [0, 1, 'R'], B : [1, B, 'R']}, 
        1 : {0 : [1, 0, 'R'], 1 : [1, 1, 'R'], B : [2, B, 'L']}, 
        2 : {0 : [2, 1, 'L'], 1 : [3, 0, 'L'], B : [5, B, 'R']}, 
        3 : {0 : [3, 0, 'L'], 1 : [3, 1, 'L'], B : [4, B, 'L']}, 
        4 : {0 : [0, 1, 'R'], 1 : [4, 0, 'L'], B : [0, 1, 'R']},
        5 : {1 : [5, B, 'R'], B : ["H", B, 'N']}, 
        }



M = TuringMachine(Q, S, G, B, Start, Finish, Delta, [1, 1, 0, 1, B, 1, 0, 1]) # 13 + 5
print(M)

M.run(True, 4992, 5015)
print(M.getRes())

